function for_loop() {
    const tree = [
        "              *",
        "            * * *",
        "          * * * * *",
        "        * * * * * * *",
        "      * * * * * * * * *",
        "    * * * * * * * * * * *",
        "              *",
        "              *",
        "            * * *"
    ];
    for (let i = 0; i < tree.length; i++) { // here is the for loop hehe
        console.log(tree[i]);
    }
}

for_loop();